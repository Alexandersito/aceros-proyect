// Función genérica para asignar eventos de clic a los botones
function asignarEventoClick(idBoton, tabla) {
    var boton = document.getElementById(idBoton);
    boton.addEventListener('click', function () {
        // Eliminar fondo de color naranja de todos los botones
        var botones = document.querySelectorAll('li');
        botones.forEach(function (boton) {
            boton.style.backgroundColor = '';
            boton.style.color = ''
            boton.style.fontWeight = '100'; // Restaurar fuente normal
        });

        // Asignar estilo al botón actual
        boton.style.backgroundColor = 'white';
        boton.style.color = 'black';
        boton.style.fontWeight = '900'; // Establecer fuente negrita

        mantenerBotonFlecha();
        imprimirTabla(tabla);
        categorias.classList.toggle('categorias-visible');

        window.scrollTo({
            top: 0,
            behavior: 'smooth'
        });
    });
}

